        <p>Based on the Federal <a href="http://rems.ed.gov/GuideK12.aspx" target="_blank"><em>Guide for Developing High-Quality School Emergency Operations  Plans</em></a>, EOP ASSIST is a user-friendly tool that walks school and district emergency  management practitioners through the <em>Guide&rsquo;s</em> recommended six step planning process for creating a customized and downloadable  school emergency operations plan (EOP).</p>
        <p>Before initiating the process of developing a  school EOP, it is recommended that EOP ASSIST users familiarize themselves with  the <em>Guide</em> by completing any or all of  the following activities:</p>
        <ul>
            <ul>
                <ul>
                    <li>Download and print a copy of the <a href="http://rems.ed.gov/GuideK12.aspx" target="_blank"><em>Guide</em></a>. It will be useful to refer to this <em>Guide</em> as you progress throughout the  tool.</li>
                    <li>Read the <a href="http://rems.ed.gov/K12GuideForDevelHQSchool.aspx" target="_blank">At-a-Glance version</a> of the <em>Guide</em>. </li>
                    <li>View an archived <a href="http://rems.ed.gov/DevelopingHigh-QualityEmergencyOperationsPlans.aspx" target="_blank">webinar</a> that introduces and summarizes the <em>Guide</em> through both slides and a video.</li>
                    <li>Complete  a free, 90- to 120-minute <a href="http://rems.ed.gov/trainings/course_k12.aspx" target="_blank">online course</a> on  the <em>Guide</em>.&nbsp; </li>
                    <li>Request  a free, on-site training, <a href="http://rems.ed.gov/TA_TrainingsByRequest.aspx" target="_blank">Developing Emergency Operations Plans (EOPs) K-12 101 Training</a>, for your school, district,  and/or broader school community.</li><br>
                </ul>
            </ul>
        </ul>
        <p>In addition to becoming acquainted with the  Federal guidance on the process for developing school EOPs, it is also  recommended that EOP ASSIST users review additional school emergency management  resources, including the following:</p>
        <ul>
            <ul>
                <ul>
                    <li><a href="http://rems.ed.gov/stateresources.aspx" target="_blank">State-by-state requirements</a> for school EOPs;</li>
                    <li><a href="http://rems.ed.gov/docs/Glossary%20of%20Key%20Terms%208.8.2014.pdf" target="_blank">A glossary</a> of emergency management terminology;</li>
                    <li><a href="http://rems.ed.gov/K12NIMSImplementation.aspx" target="_blank">Guidance</a> on National Incident Management System (NIMS) Implementation Activities for  Schools; and</li>
                    <li><a href="http://rems.ed.gov/docs/Guide_for_Developing_High-Quality_School_Emergency_Operations_Plans-Resources07172013R.pdf" target="_blank">A list of resources</a> supporting the <em>Guide for Developing High-Quality School  Emergency Operations Plans</em>, which includes resources to support each of the  six steps in the recommended planning process.</li>
                </ul>
