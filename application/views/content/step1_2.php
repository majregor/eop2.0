    $(document).ready(function(){


        $("a#rightArrowButton").attr("href", "<?php echo(base_url('plan/step1/3')); ?>"); //Next

        $("a#leftArrowButton").attr("href", "<?php echo(base_url('plan/step1/1')); ?>"); //Previous


