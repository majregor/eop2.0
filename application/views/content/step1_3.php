    <p>After  the planning team has been formed, the team members should form a common  framework by taking time to learn each other&rsquo;s vocabulary, command structure,  and culture. Organizational differences may affect the expectations of different  members of the planning team, so it is important to acknowledge these  differences at the start of a collaborative process. For example, schools often  operate according to an academic calendar and daily schedule that differ from a  typical business or government calendar and daily schedule. Establishing a  common understanding about different planning team members&rsquo; availability and  schedules will help to facilitate effective planning and collaboration.</p>
    <p>By  establishing a common framework, team members will also be able to communicate  more effectively with one another. One common framework that is particularly  effective in the context of school emergency management is the National Incident Management System (NIMS). Developed by  the U.S. Department of Homeland Security, NIMS is a standardized approach used  by Federal, State, and local agencies&mdash;including K-12 schools&mdash;for responding to  emergencies. One important component of NIMS is the Incident Command System  (ICS), which clearly defines the command structure used in an emergency. School  personnel need to be trained in NIMS and their role in Incident Command in  order to more effectively work with the responders in their communities. Using  the framework of NIMS beginning in the EOP planning process will not only help  members of the planning team communicate with one another, but will also help  the team develop high-quality plans that can be clearly understood by  responders who are not members of the planning team. </p>
    <p>In  addition to forming a common framework, your planning team should take time to  define and assign roles and responsibilities for each person involved in the  development and refinement of the plan. This will help to facilitate greater  coordination among team members.</p>
